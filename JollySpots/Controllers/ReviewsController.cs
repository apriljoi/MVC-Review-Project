using System;
using System.Collections.Generic;
using System.Data;
using System.Data.Entity;
using System.Linq;
using System.Net;
using System.Web;
using System.Web.Mvc;
using JollySpots.Models;
//I didn't change anything in this file. It is full of methods and if statements, which do make sense
